package com.example.listviewdemo;

public class Modelclass {

    String texthead;
    String text1;
    String text2;
    String text3;
    int imagedp;
    int imagestar;

    public Modelclass() {
        this.texthead = texthead;
        this.text1 = text1;
        this.text2 = text2;
        this.text3 = text3;
        this.imagedp = imagedp;
        this.imagestar = imagestar;

    }

    public String getTexthead() { return texthead; }

    public void setTexthead(String texthead) { this.texthead = texthead; }

    public String getText1() { return text1; }

    public void setText1(String text1) { this.text1 = text1; }

    public String getText2() { return text2; }

    public void setText2(String text2) { this.text2 = text2; }

    public String getText3() { return text3; }

    public void setText3(String text3) { this.text3 = text3; }

    public int getImagedp() { return imagedp; }

    public void setImagedp(int imagedp) { this.imagedp = imagedp; }

    public int getImagestar() { return imagestar; }

    public void setImagestar(int imagestar) { this.imagestar = imagestar; }


}